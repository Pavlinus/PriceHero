<article>
<p>
В Dishonored 2, новой главе саги Dishonored, создаваемой командой Arkane Studios, вы снова окажетесь в роли убийцы со сверхъестественными способностями. Вы вернетесь в удивительный мир, сочетающий магию, мистику и технологии. Решите, за кого играть: за императрицу Эмили Колдуин или за защитника короны, Корво Аттано. Выберите стиль игры: скрывайтесь в тенях, незаметно обходя врагов, используйте весь арсенал убийственных приемов или сочетайте эти два стиля, как вам нравится. Подберите уникальное сочетание способностей, механизмов и снаряжения, чтобы с легкостью уничтожать врагов. Игра реагирует на каждое ваше решение, и в зависимости от ваших действий у каждого задания может быть разный исход. 
</p>
<br>
<p>
Действие игры Dishonored 2 разворачивается спустя пятнадцать лет после победы над лордом-регентом и избавления от ужасной крысиной чумы. Необычный враг отнял трон у императрицы Эмили Колдуин, и мрачная тень злого рока нависла над Островами. Играя за Эмили или Корво, вы окажетесь на легендарных улицах Дануолла и в Карнаке - некогда прекрасном портовом городе, который сыграет в этой истории ключевую роль. Метка Чужого и ваши новые способности помогут выследить ваших врагов и вернуть то, что принадлежит вам по праву.
</p>
