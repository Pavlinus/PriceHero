Откройте для себя новейшее приключение от Pendulo Studios, создателей серии Runaway! 1481. Юного Джона унижают и публично тащат по улице. Он обвинен в колдовстве испанской инквизицией и брошен в тюрьму, где его подвергнут пыткам. Позже с ним произойдет алхимическая трансмутация, благодаря которой он обретет бессмертие. Но не все прошло так, как предполагалось. Поэтому после каждого воскрешения Джон теряет всю память о себе. 500 лет спустя, в наши дни, Джон и Полин ищут артефакт, необходимый для того, чтобы воспроизвести сатанинский ритуал. Во время этого расследования им придется противостоять новым опасным персонажам, стремящимся вырвать у них их секреты... Присоединитесь к бессмертным Джону Йестедею и Полин Пети в игре Yesterday Origins и окунитесь в эпическое приключение в различных эпохах и странах в этой истории, полной интриг и неожиданных поворотов сюжета.
